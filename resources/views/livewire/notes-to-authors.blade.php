                </h2>
                <p>
                    Trends in Libraries, Information Science and Technology
                    (TLIST) is the official journal of Enugu State Chapter of
                    Nigeria Library Association. As a main line professional
                    journal, it is designed as a major outlet for dissemination
                    scholarly research of librarians, information scientists,
                    information managers, archivists and other professionals in
                    the information industry within Nigeria and internationally.
                    It is a bi-annual journal and accepts well researched
                    empirical and position papers from all areas in Library and
                    Information Science particularly current trends in the
                    profession.
                </p>
