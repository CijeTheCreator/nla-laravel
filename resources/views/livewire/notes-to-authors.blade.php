            <div>
                <h2 class="text-2xl font-serif text-[#6ba439] mb-4">
                    Peer Review Process
                </h2>
                <p>
                    As a referred journal, TLIST adopts double-blind peer review
                    mechanism. The reviewers do not have any information about
                    the authors and authors do not know the reviewers. So papers
                    are sent to the reviewers as anonymous authors by removing
                    any information including authors' affiliations. All papers
                    are first screened by the editor and those found worthy are
                    sent out for peer review process, while papers outside the
                    scope of the journal or do not meet the author guidelines
                    are rejected at the screening stage and authors notified
                    immediately. On the recommendation of the reviewers,
                    manuscripts are accepted, rejected, or sent back to the
                    authors for revision and resubmission. In cases where the
                    feedback is varied or less substantial, the editor-in-chief
                    may either closely review the submission and reconcile the
                    feedback or seek a third reviewer.
                </p>
                <p>
                    In the process of peer review, the main considerations of
                    the reviewers include:
                </p>
                <ul class="list-disc pl-6 space-y-1">
                    <li>
                        Has the manuscript made a new contribution to the body
                        of knowledge in the area?
                    </li>
                    <li>Is the method adopted for the study appropriate?</li>
                    <li>Does the evidence presented support the hypothesis?</li>
                    <li>
                        How clear has the author communicated to the targeted
                        readers of the journal?
                    </li>
                    <li>
                        Does the literature review place the research or
                        opinions in perspective?
                    </li>
                </ul>
                <p>
                    The journal review process lasts for about six to eight
                    weeks and the editor communicates with the author
                    immediately a decision has been taken. Authors are not
                    expected to submit the same manuscript to another journal
                    while the review is in progress.
                </p>
            </div>

